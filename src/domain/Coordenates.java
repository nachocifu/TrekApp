package domain;

public class Coordenates {
	double x;
	double y;
	
	public Coordenates(double x, double y){
		this.x=x;
		this.y=y;
	}
	
	public Double getX(){
		return this.x;
	}
	
	public void setX(Double x){
		this.x=x;
	}
	
	public Double getY(){
		return this.y;
	}
	public void setY(Double y){
		this.y=y;
	}
}
